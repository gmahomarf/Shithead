$(document)
.ready(
		function()
		{
			$('#frmChat')
			.submit(
			    function(e)
			    {
			        $.post(
			            '/msg',
			            'msg='+$('#msg').val(),
			            function()
			            {
			                $('#msg').val('');
			            },
			            'json'
			            )
			        return false;
			    }
			    );
			var socket = io.connect();
			socket.on('chatMsg',
			    function(data)
			    {
			        $('#chat')
			        .append(
			            $('<p></p>')
			            .append(
			            		$('<span></span>').addClass('name').html(data.name)
	                )
	                .append(
	                    $('<span></span>').addClass('text').html(data.text)
                  )
              );
			    }
		    );
		}
	);